sem = int(input("Digite um número para saber o deia da semana, (1-7): "))

if sem == 1:
    print("Dia = Domingo.")
elif sem == 2:
    print("Dia = Segunda.")
elif sem == 3:
    print("Dia = Terça.")
elif sem == 4:
    print("Dia = Quarta.")
elif sem == 5:
    print("Dia = Quinta.")
elif sem == 6:
    print("Dia = Sexta.")
elif sem == 7:
    print("Dia = Sábado.")
else:
    print("Valor inválido!")